package com.example.springreactivepoc.rest;

import com.example.springreactivepoc.kafka.EventSender;
import com.example.springreactivepoc.kafka.message.StatusChangeRequest;
import lombok.extern.slf4j.Slf4j;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RestController;
import reactor.core.publisher.Mono;

@Slf4j
@RestController
public class RequestController {

    private final EventSender eventSender;

    public RequestController(EventSender eventSender) {
        this.eventSender = eventSender;
    }

    @PostMapping("/request")
    public Mono<Void> save(@RequestBody StatusChangeRequest request) {
        return eventSender.sendRequest(request);
    }
}
